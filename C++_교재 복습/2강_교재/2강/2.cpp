#include <iostream>

using namespace std;

int main(void)
{
	cout << "Hello\n";
	cout << "Hi\tfriends." << endl;
	cout << "Buenos dias  \banimgos." << endl;
	cout << "Hello\rBonjour mes amis." << endl;
	cout << "This is a single quote \'." << endl;
	cout << "This is a double quote \'." << endl;
	cout << "This is how to print a backslash \\." << endl;

	return 0;
}


